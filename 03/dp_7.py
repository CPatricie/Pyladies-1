from turtle import forward, left, right, exitonclick


for i in range(10): 
    forward(50)
    left(90)
    forward(50)
    left(90)
    forward(50)
    right(120)
    forward(50)
    right(120)
    forward(50)
    right(75)
    forward(2**(1/2)*50)
    right(135)
    forward(50)
    right(135)
    forward(2**(1/2)*50)
    left(45)
    forward(25)

exitonclick()